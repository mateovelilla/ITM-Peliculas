    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        peliculas.negocio.director directorController = new peliculas.negocio.director();
        String nombre = JTextNombre.getText();
        String nacion = JTextNacion.getText();
        String fechaNacimiento = JTextFnac.getText();
        Boolean respuesta = directorController.Guardar(nombre, nacion, fechaNacimiento);
        if (respuesta)
        {
            limpiarCampos();
            JOptionPane.showMessageDialog(null, "Se Guardo con exito","Mensaje de alerta",JOptionPane.QUESTION_MESSAGE);
        }else
        {
            JOptionPane.showMessageDialog(null,directorController.getResponse() ,"Mensaje de alerta",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_GuardarActionPerformed

